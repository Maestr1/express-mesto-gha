const { celebrate } = require('celebrate');
const Joi = require('joi');

module.exports.validateCardBody = celebrate({
  body: Joi.object()
    .keys({
      name: Joi.string()
        .required()
        .min(2)
        .max(30),
      link: Joi.string()
        .required(),
    }),
});

module.exports.validateUserBody = celebrate({
  body: Joi.object()
    .keys({
      name: Joi.string()
        .required()
        .min(2)
        .max(30),
      about: Joi.string()
        .required()
        .min(2)
        .max(30),
    })
    .unknown(true),
});
